divide <- function(numerator, denominator) {
  numerator / denominator
}

divide(3, 2)                       # 1.5
divide(numerator=3, denominator=2) # 1.5
divide(n=3, d=2)                   # 1.5
divide(den=3, num=2)               # 0.66
divide(den=3, 2)                   # 0.66
divide(3, num=2)                   # 0.66
